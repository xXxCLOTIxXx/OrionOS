#ifndef COLORS_H
#define COLORS_H

#include <stdint.h>

namespace Color {
    constexpr uint8_t Black = 0x0;          // Чёрный
    constexpr uint8_t DarkBlue = 0x1;       // Тёмно-синий
    constexpr uint8_t DarkGreen = 0x2;      // Тёмно-зелёный
    constexpr uint8_t DarkCyan = 0x3;       // Тёмно-бирюзовый
    constexpr uint8_t DarkRed = 0x4;        // Тёмно-красный
    constexpr uint8_t DarkMagenta = 0x5;    // Тёмно-фиолетовый
    constexpr uint8_t DarkYellow = 0x6;     // Тёмно-жёлтый
    constexpr uint8_t LightGrey = 0x7;      // Серый
    constexpr uint8_t DarkGrey = 0x8;       // Тёмно-серый
    constexpr uint8_t LightBlue = 0x9;      // Светло-синий
    constexpr uint8_t LightGreen = 0xA;     // Светло-зелёный
    constexpr uint8_t LightCyan = 0xB;      // Светло-бирюзовый
    constexpr uint8_t LightRed = 0xC;       // Светло-красный
    constexpr uint8_t LightMagenta = 0xD;   // Светло-фиолетовый
    constexpr uint8_t LightYellow = 0xE;    // Светло-жёлтый
    constexpr uint8_t White = 0xF;          // Белый
}

#endif
